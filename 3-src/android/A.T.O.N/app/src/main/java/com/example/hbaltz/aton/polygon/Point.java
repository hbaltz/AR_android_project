package com.example.hbaltz.aton.polygon;

/**
 * Point on 2D landscape
 */
public class Point
{
	////////////////////////////////////////////////////////////////////////////////////////////////
	///////////////////////////////////////// FIELDS: //////////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////

	public float x;
	public float y;

	////////////////////////////////////////////////////////////////////////////////////////////////
	///////////////////////////////////////// CONSTRUCTORS: ////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////

    public Point() {}

	public Point(float x, float y)
	{
		this.x = x;
		this.y = y;
	}

    ////////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////// GETTERS: /////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////

    public float getX() {
        return this.x;
    }

    public float getY() {
        return this.y;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////// SETTERS: /////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////

    public void setX(float x) {
        this.x = x;
    }

    public void setY(float y) {
        this.y = y;
    }

    public void setXY(float x, float y){
        this.x = x;
        this.y = y;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////
	///////////////////////////////////////// METHODS: /////////////////////////////////////////////
	////////////////////////////////////////////////////////////////////////////////////////////////

	@Override
	public String toString()
	{
		return String.format("(%.2f,%.2f)", x, y);
	}
}