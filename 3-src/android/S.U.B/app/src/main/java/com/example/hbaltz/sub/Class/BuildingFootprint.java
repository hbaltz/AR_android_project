package com.example.hbaltz.sub.Class;

import com.esri.core.geometry.Polygon;

/**
 * Created by hbaltz on 6/14/2016.
 */
public class BuildingFootprint {
    ////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////// FIELDS: ///////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////

    private Polygon footprint;
    private double length;
    private double area;

    ////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////// CONSTRUCTORS: /////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////

    public BuildingFootprint(){
        this.footprint = new Polygon();
        this.length = 0d;
        this.area = 0d;
    }

    public BuildingFootprint(Polygon ft, double lgth, double area){
        this.footprint = ft;
        this.length = lgth;
        this.area = area;
    }


    ////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////// GETTERS: //////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////

    public Polygon getFootprint() {
        return this.footprint;
    }

    public double getLength() {
        return this.length;
    }

    public double getArea() {
        return this.area;
    }

    ////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////// SETTERS: //////////////////////////////////////////////////
    ////////////////////////////////////////////////////////////////////////////////////////////////

    public void setFootprint(Polygon footprint) {
        this.footprint = footprint;
    }

    public void setLength(double length) {
        this.length = length;
    }

    public void setArea(double area) {
        this.area = area;
    }
}
